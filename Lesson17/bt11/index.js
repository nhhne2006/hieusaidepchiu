
// Math.abs() Trả về giá trị tuyệt đối và sẽ bỏ dấu âm nếu có...

alert(Math.abs(-5.5)); // kết quả 5.5
// -5.5 là số âm nên Math.abs() sẽ trả về số dương
alert(Math.abs(10)); // kết quả 10
// 10 là số dương nên nó sẽ giữ nguyên







// Math.ceil() Làm tròn LÊN số nguyên gần nhất...

console.log(Math.ceil(4.4)); // Kết quả: 5
// 4.4 làm tròn lên thành 5
console.log(Math.ceil(5.8)); // Kết quả: 6
// 5.8 làm tròn lên thành 6







// Math.floor() Làm tròn XUỐNG số nguyên gần nhất...

console.log(Math.floor(5.7)); // Kết quả: 5
// 5.7 làm tròn xuống thành 5
console.log(Math.floor(6.1)); // Kết quả: 6
// 6.1 làm tròn xuống thành 6






// Math.round() Làm tròn về số nguyên gần nhất. Nếu số sau dấu thập phân >= 0.5 thì làm tròn lên, ngược lại làm tròn xuống...

console.log(Math.round(5.1)); // Kết quả: 5
// 0.1 < 0.5 nên làm tròn xuống
console.log(Math.round(20.6)); // Kết quả: 21
// 0.6 >= 0.5 nên làm tròn lên



